public class LuasLing {
    
    public double luasLing(double jari){
        double luasling;
        luasling = 3.14 * jari * jari;
        return luasling;
    }
    public double kelilingLing(double jari){
        double kelilingLing ;
        kelilingLing = 2 * 3.14 * jari;
        return kelilingLing;
    }
}